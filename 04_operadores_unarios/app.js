// operadores unarios

let dato = 5;
dato = dato + 1;

console.log(dato);

// dato unario en 1
dato++;
console.log(dato);

// dato unario en -1
dato--;
console.log(dato);

// quiero aumentar en un valor n
dato += 4;
console.log(dato);

// quiero quitar en un valor n
dato -= 7;
console.log(dato);



